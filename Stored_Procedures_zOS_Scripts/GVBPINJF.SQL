CREATE PROCEDURE INSLPSRCFLD (
  IN DOC CLOB, 
  IN IMPORT SMALLINT)

RESULT SETS 0
LANGUAGE SQL
DISABLE DEBUG MODE

BEGIN
 DECLARE PDOC XML;
 SET PDOC = XMLPARSE(DOCUMENT DOC PRESERVE WHITESPACE);
 
-- If normal create
 IF IMPORT = 0 THEN
 
  INSERT INTO LOOKUPSRCKEY (
    ENVIRONID,LOOKUPSTEPID,KEYSEQNBR,FLDTYPE,
    LRFIELDID,LRLFASSOCID,LOOKUPID,VALUEFMTCD,
    SIGNED,VALUELEN,DECIMALCNT,FLDCONTENTCD,
    ROUNDING,JUSTIFYCD,MASK,SYMBOLICNAME,VALUE,
    CREATEDTIMESTAMP,CREATEDUSERID,
    LASTMODTIMESTAMP,LASTMODUSERID)
   SELECT X.ENVIRONID,X.LOOKUPSTEPID,X.KEYSEQNBR,X.FLDTYPE,
    X.LRFIELDID,X.LRLFASSOCID,X.LOOKUPID,X.VALUEFMTCD,
    X.SIGNED,X.VALUELEN,X.DECIMALCNT,X.FLDCONTENTCD,
    X.ROUNDING,X.JUSTIFYCD,X.MASK,X.SYMBOLICNAME,X.VALUE,
    CURRENT TIMESTAMP,X.CREATEDUSERID,
    CURRENT TIMESTAMP,X.LASTMODUSERID
   FROM XMLTABLE('$doc/Root/Record' passing PDOC as "doc"
   COLUMNS 
     "ENVIRONID"        INT PATH 'ENVIRONID',
     "LOOKUPSTEPID"     INT PATH 'LOOKUPSTEPID',
     "KEYSEQNBR"        INT PATH 'KEYSEQNBR',
     "FLDTYPE"          INT PATH 'FLDTYPE',
     "LRFIELDID"        INT PATH 'LRFIELDID',
     "LRLFASSOCID"      INT PATH 'LRLFASSOCID',
     "LOOKUPID"         INT PATH 'LOOKUPID',
     "VALUEFMTCD"       VARCHAR(5) PATH 'VALUEFMTCD',
     "SIGNED"           SMALLINT PATH 'SIGNED',
     "VALUELEN"         INT PATH 'VALUELEN',
     "DECIMALCNT"       INT PATH 'DECIMALCNT',
     "FLDCONTENTCD"     VARCHAR(5) PATH 'FLDCONTENTCD',
     "ROUNDING"         INT PATH 'ROUNDING',
     "JUSTIFYCD"        VARCHAR(5) PATH 'JUSTIFYCD',
     "MASK"             VARCHAR(48) PATH 'MASK',
     "SYMBOLICNAME"     VARCHAR(254) PATH 'SYMBOLICNAME',
     "VALUE"            VARCHAR(254) PATH 'VALUE',
     "CREATEDUSERID"    VARCHAR(8) PATH 'CREATEDUSERID',
     "LASTMODUSERID"    VARCHAR(8) PATH 'LASTMODUSERID') AS X;
     
 -- Creation from an import or migration
 ELSE
 
  INSERT INTO LOOKUPSRCKEY (
    ENVIRONID,LOOKUPSTEPID,KEYSEQNBR,FLDTYPE,
    LRFIELDID,LRLFASSOCID,LOOKUPID,VALUEFMTCD,
    SIGNED,VALUELEN,DECIMALCNT,FLDCONTENTCD,
    ROUNDING,JUSTIFYCD,MASK,SYMBOLICNAME,VALUE,
    CREATEDTIMESTAMP,CREATEDUSERID,
    LASTMODTIMESTAMP,LASTMODUSERID)
   SELECT X.ENVIRONID,X.LOOKUPSTEPID,X.KEYSEQNBR,X.FLDTYPE,
    X.LRFIELDID,X.LRLFASSOCID,X.LOOKUPID,X.VALUEFMTCD,
    X.SIGNED,X.VALUELEN,X.DECIMALCNT,X.FLDCONTENTCD,
    X.ROUNDING,X.JUSTIFYCD,X.MASK,X.SYMBOLICNAME,X.VALUE,
    X.CREATEDTIMESTAMP,X.CREATEDUSERID,
    X.LASTMODTIMESTAMP,X.LASTMODUSERID
   FROM XMLTABLE('$doc/Root/Record' passing PDOC as "doc"
   COLUMNS 
     "ENVIRONID"        INT PATH 'ENVIRONID',
     "LOOKUPSTEPID"     INT PATH 'LOOKUPSTEPID',
     "KEYSEQNBR"        INT PATH 'KEYSEQNBR',
     "FLDTYPE"          INT PATH 'FLDTYPE',
     "LRFIELDID"        INT PATH 'LRFIELDID',
     "LRLFASSOCID"      INT PATH 'LRLFASSOCID',
     "LOOKUPID"         INT PATH 'LOOKUPID',
     "VALUEFMTCD"       VARCHAR(5) PATH 'VALUEFMTCD',
     "SIGNED"           SMALLINT PATH 'SIGNED',
     "VALUELEN"         INT PATH 'VALUELEN',
     "DECIMALCNT"       INT PATH 'DECIMALCNT',
     "FLDCONTENTCD"     VARCHAR(5) PATH 'FLDCONTENTCD',
     "ROUNDING"         INT PATH 'ROUNDING',
     "JUSTIFYCD"        VARCHAR(5) PATH 'JUSTIFYCD',
     "MASK"             VARCHAR(48) PATH 'MASK',
     "SYMBOLICNAME"     VARCHAR(254) PATH 'SYMBOLICNAME',
     "VALUE"            VARCHAR(254) PATH 'VALUE',
     "CREATEDTIMESTAMP" TIMESTAMP PATH 'CREATEDTIMESTAMP',
     "CREATEDUSERID"    VARCHAR(8) PATH 'CREATEDUSERID',
     "LASTMODTIMESTAMP" TIMESTAMP PATH 'LASTMODTIMESTAMP',
     "LASTMODUSERID"    VARCHAR(8) PATH 'LASTMODUSERID') AS X;
 END IF;

END
