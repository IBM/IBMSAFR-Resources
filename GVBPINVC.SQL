CREATE PROCEDURE INSVIEWCOL (  
  IN DOC CLOB, 
  IN IMPORT SMALLINT)
  
RESULT SETS 0
LANGUAGE SQL
DISABLE DEBUG MODE

BEGIN
 DECLARE RET INT DEFAULT 0;
 DECLARE PDOC XML;
 SET PDOC = XMLPARSE(DOCUMENT DOC PRESERVE WHITESPACE);

-- If normal create
 IF IMPORT = 0 THEN

  INSERT INTO VIEWCOLUMN (
      ENVIRONID,VIEWID,COLUMNNUMBER,
      FLDFMTCD,SIGNEDIND,STARTPOSITION,MAXLEN,
      ORDINALPOSITION,DECIMALCNT,ROUNDING,
      FLDCONTENTCD,JUSTIFYCD,DEFAULTVAL,
      VISIBLE,SUBTOTALTYPECD,SPACESBEFORECOLUMN,
      EXTRACTAREACD,EXTRAREAPOSITION,
      SUBTLABEL,RPTMASK,
      HDRJUSTIFYCD,HDRLINE1,HDRLINE2,HDRLINE3,
      FORMATCALCLOGIC,
      CREATEDTIMESTAMP,CREATEDUSERID,
      LASTMODTIMESTAMP,LASTMODUSERID)
   SELECT X.ENVIRONID,X.VIEWID,X.COLUMNNUMBER,
      X.FLDFMTCD,X.SIGNEDIND,X.STARTPOSITION,X.MAXLEN,
      X.ORDINALPOSITION,X.DECIMALCNT,X.ROUNDING,
      X.FLDCONTENTCD,X.JUSTIFYCD,X.DEFAULTVAL,
      X.VISIBLE,X.SUBTOTALTYPECD,X.SPACESBEFORECOLUMN,
      X.EXTRACTAREACD,X.EXTRAREAPOSITION,
      X.SUBTLABEL,X.RPTMASK,
      X.HDRJUSTIFYCD,X.HDRLINE1,X.HDRLINE2,X.HDRLINE3,
      X.FORMATCALCLOGIC,
      CURRENT TIMESTAMP,X.CREATEDUSERID,
      CURRENT TIMESTAMP,X.LASTMODUSERID
   FROM XMLTABLE('$doc/Root/Record' passing PDOC as "doc"
   COLUMNS 
     "ENVIRONID"          INT PATH 'ENVIRONID',
     "VIEWID"             INT PATH 'VIEWID',
     "COLUMNNUMBER"       INT PATH 'COLUMNNUMBER',
     "FLDFMTCD"           CHAR(5) PATH 'FLDFMTCD',
     "SIGNEDIND"          SMALLINT PATH 'SIGNEDIND',
     "STARTPOSITION"      INT PATH 'STARTPOSITION',
     "MAXLEN"             INT PATH 'MAXLEN',
     "ORDINALPOSITION"    INT PATH 'ORDINALPOSITION',
     "DECIMALCNT"         INT PATH 'DECIMALCNT',
     "ROUNDING"           INT PATH 'ROUNDING',
     "FLDCONTENTCD"       CHAR(5) PATH 'FLDCONTENTCD',
     "JUSTIFYCD"          CHAR(5) PATH 'JUSTIFYCD',
     "DEFAULTVAL"         VARCHAR(254) PATH 'DEFAULTVAL',
     "VISIBLE"            SMALLINT PATH 'VISIBLE',
     "SUBTOTALTYPECD"     CHAR(5) PATH 'SUBTOTALTYPECD',
     "SPACESBEFORECOLUMN" INT PATH 'SPACESBEFORECOLUMN',
     "EXTRACTAREACD"      CHAR(5) PATH 'EXTRACTAREACD',
     "EXTRAREAPOSITION"   INT PATH 'EXTRAREAPOSITION',
     "SUBTLABEL"          VARCHAR(48) PATH 'SUBTLABEL',
     "RPTMASK"            VARCHAR(48) PATH 'RPTMASK',
     "HDRJUSTIFYCD"       CHAR(5) PATH 'HDRJUSTIFYCD',
     "HDRLINE1"           VARCHAR(254) PATH 'HDRLINE1',
     "HDRLINE2"           VARCHAR(254) PATH 'HDRLINE2',
     "HDRLINE3"           VARCHAR(254) PATH 'HDRLINE3',
     "FORMATCALCLOGIC"    CLOB(32 M) PATH 'FORMATCALCLOGIC',
     "CREATEDUSERID"      VARCHAR(8) PATH 'CREATEDUSERID',
     "LASTMODUSERID"      VARCHAR(8) PATH 'LASTMODUSERID') AS X;

 -- Creation from an import or migration
 ELSE
   
  INSERT INTO VIEWCOLUMN (
      ENVIRONID,VIEWCOLUMNID,VIEWID,COLUMNNUMBER,
      FLDFMTCD,SIGNEDIND,STARTPOSITION,MAXLEN,
      ORDINALPOSITION,DECIMALCNT,ROUNDING,
      FLDCONTENTCD,JUSTIFYCD,DEFAULTVAL,
      VISIBLE,SUBTOTALTYPECD,SPACESBEFORECOLUMN,
      EXTRACTAREACD,EXTRAREAPOSITION,
      SUBTLABEL,RPTMASK,
      HDRJUSTIFYCD,HDRLINE1,HDRLINE2,HDRLINE3,
      FORMATCALCLOGIC,
      CREATEDTIMESTAMP,CREATEDUSERID,
      LASTMODTIMESTAMP,LASTMODUSERID)
   SELECT X.ENVIRONID,X.VIEWCOLUMNID,X.VIEWID,X.COLUMNNUMBER,
      X.FLDFMTCD,X.SIGNEDIND,X.STARTPOSITION,X.MAXLEN,
      X.ORDINALPOSITION,X.DECIMALCNT,X.ROUNDING,
      X.FLDCONTENTCD,X.JUSTIFYCD,X.DEFAULTVAL,
      X.VISIBLE,X.SUBTOTALTYPECD,X.SPACESBEFORECOLUMN,
      X.EXTRACTAREACD,X.EXTRAREAPOSITION,
      X.SUBTLABEL,X.RPTMASK,
      X.HDRJUSTIFYCD,X.HDRLINE1,X.HDRLINE2,X.HDRLINE3,
      X.FORMATCALCLOGIC,
      X.CREATEDTIMESTAMP,X.CREATEDUSERID,
      X.LASTMODTIMESTAMP,X.LASTMODUSERID
   FROM XMLTABLE('$doc/Root/Record' passing PDOC as "doc"
   COLUMNS 
     "ENVIRONID"          INT PATH 'ENVIRONID',
     "VIEWCOLUMNID"       INT PATH 'VIEWCOLUMNID',
     "VIEWID"             INT PATH 'VIEWID',
     "COLUMNNUMBER"       INT PATH 'COLUMNNUMBER',
     "FLDFMTCD"           CHAR(5) PATH 'FLDFMTCD',
     "SIGNEDIND"          SMALLINT PATH 'SIGNEDIND',
     "STARTPOSITION"      INT PATH 'STARTPOSITION',
     "MAXLEN"             INT PATH 'MAXLEN',
     "ORDINALPOSITION"    INT PATH 'ORDINALPOSITION',
     "DECIMALCNT"         INT PATH 'DECIMALCNT',
     "ROUNDING"           INT PATH 'ROUNDING',
     "FLDCONTENTCD"       CHAR(5) PATH 'FLDCONTENTCD',
     "JUSTIFYCD"          CHAR(5) PATH 'JUSTIFYCD',
     "DEFAULTVAL"         VARCHAR(254) PATH 'DEFAULTVAL',
     "VISIBLE"            SMALLINT PATH 'VISIBLE',
     "SUBTOTALTYPECD"     CHAR(5) PATH 'SUBTOTALTYPECD',
     "SPACESBEFORECOLUMN" INT PATH 'SPACESBEFORECOLUMN',
     "EXTRACTAREACD"      CHAR(5) PATH 'EXTRACTAREACD',
     "EXTRAREAPOSITION"   INT PATH 'EXTRAREAPOSITION',
     "SUBTLABEL"          VARCHAR(48) PATH 'SUBTLABEL',
     "RPTMASK"            VARCHAR(48) PATH 'RPTMASK',
     "HDRJUSTIFYCD"       CHAR(5) PATH 'HDRJUSTIFYCD',
     "HDRLINE1"           VARCHAR(254) PATH 'HDRLINE1',
     "HDRLINE2"           VARCHAR(254) PATH 'HDRLINE2',
     "HDRLINE3"           VARCHAR(254) PATH 'HDRLINE3',
     "FORMATCALCLOGIC"    CLOB(32 M) PATH 'FORMATCALCLOGIC',
     "CREATEDTIMESTAMP"   TIMESTAMP PATH 'CREATEDTIMESTAMP',
     "CREATEDUSERID"      VARCHAR(8) PATH 'CREATEDUSERID',
     "LASTMODTIMESTAMP"   TIMESTAMP PATH 'LASTMODTIMESTAMP',
     "LASTMODUSERID"      VARCHAR(8) PATH 'LASTMODUSERID') AS X;
 
 END IF;
 
SET RET=(SELECT IDENTITY_VAL_LOCAL() AS IDENTITY FROM SYSIBM.SYSDUMMY1);
RETURN RET;
END